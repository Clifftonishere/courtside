// ── General NBA analysis (no specific matchup required) ─────────────────────

async function analyzeGeneral(query, teams, gameDate, detail, reasoning, startTime) {
  // Build context from whatever teams/data we have
  const contextParts = [];

  // If 1 team found, load its context
  if (teams.length === 1) {
    reasoning.push(`Single team detected: ${teams[0]}`);
    try {
      const stars = (STAR_PLAYERS[teams[0]] || []).slice(0, 3);
      const ctx = await buildContext(stars[0] || teams[0], 'points', '', gameDate, { team: teams[0] });
      if (ctx) contextParts.push(formatContextForPrompt(ctx));
      const coach = getCoachingProfile(teams[0]);
      if (coach) contextParts.push(`${teams[0]} COACH: ${coach.coach} (pace: ${coach.pace}, defense: ${coach.defensiveScheme}, clutch: ${coach.clutchRating})`);
    } catch (e) {
      reasoning.push(`Context error: ${e.message}`);
    }
  }

  // Load coaching profiles for all mentioned teams or top contenders for general questions
  const topContenders = ['BOS', 'OKC', 'CLE', 'DEN', 'NYK', 'MIL', 'LAL', 'HOU', 'MIN', 'PHX'];
  const relevantTeams = teams.length > 0 ? teams : topContenders;
  const coachingSummary = relevantTeams.map(t => {
    const c = getCoachingProfile(t);
    return c ? `${t}: ${c.coach} (pace: ${c.pace}, clutch: ${c.clutchRating})` : null;
  }).filter(Boolean).join('\n');

  // Get today's games for context
  let todayGames = '';
  try {
    const https = require('https');
    const raw = await new Promise((resolve, reject) => {
      https.get('https://cdn.nba.com/static/json/liveData/scoreboard/todaysScoreboard_00.json',
        { headers: { 'User-Agent': 'Mozilla/5.0' }, timeout: 5000 },
        res => { let d = ''; res.on('data', c => d += c); res.on('end', () => resolve(d)); }
      ).on('error', reject);
    });
    const sb = JSON.parse(raw);
    todayGames = (sb.scoreboard?.games || []).map(g =>
      `${g.awayTeam.teamTricode} @ ${g.homeTeam.teamTricode} (${g.gameStatusText})`
    ).join(', ');
  } catch { /* ignore */ }

  // Load playoff urgency for top contenders if general question
  let standingsContext = '';
  if (teams.length === 0) {
    try {
      const { getPlayoffUrgency } = require('./factors/playoff-urgency');
      const urgencies = await Promise.all(topContenders.slice(0, 6).map(async t => {
        const u = await getPlayoffUrgency(t, gameDate);
        return u ? `${t}: ${u.tier} (${(u.urgency * 100).toFixed(0)}%) — ${u.reason}` : null;
      }));
      standingsContext = urgencies.filter(Boolean).join('\n');
    } catch { /* ignore */ }
  }

  const userPrompt = `User query: "${query}"

${todayGames ? `TODAY'S GAMES: ${todayGames}` : ''}

${contextParts.length > 0 ? 'TEAM CONTEXT:\n' + contextParts.join('\n\n') : ''}

${coachingSummary ? 'COACHING PROFILES:\n' + coachingSummary : ''}

${standingsContext ? 'PLAYOFF STANDINGS/URGENCY:\n' + standingsContext : ''}

Date: ${gameDate}

Provide a thorough, objective, data-driven analysis responding to the user's question. Use the NBA data above where relevant.${detail === 'brief' ? ' Keep it concise — 250 words max.' : ''}`;

  let analysis = '';
  try {
    analysis = await callClaude(ANALYSIS_MODEL, ANALYSIS_SYSTEM_PROMPT, userPrompt, detail === 'brief' ? 800 : 2000);
    reasoning.push(`General analysis generated via ${ANALYSIS_MODEL}`);
  } catch (e) {
    reasoning.push(`Analysis failed: ${e.message}`);
    analysis = 'Unable to generate analysis — please try again.';
  }

  if (!analysis) analysis = 'Unable to generate analysis — please try again.';

  // Append disclaimer
  analysis += DISCLAIMER;

  return {
    analysis,
    prices: [],
    context: {
      teams,
      date: gameDate,
      type: teams.length === 1 ? 'team_analysis' : 'general_nba',
    },
    reasoning,
    meta: {
      query,
      teams,
      date: gameDate,
      prices_count: 0,
      duration_ms: Date.now() - startTime,
      model: ANALYSIS_MODEL,
      type: teams.length === 1 ? 'team_analysis' : 'general_nba',
    },
  };
}

